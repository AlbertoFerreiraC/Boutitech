    private void jrCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jrCodigoActionPerformed
        ConectoBD conectar = new ConectoBD();
        conectar.Conectado();
        jtBusqueda.setText("");
        codigo = new TextAutoCompleter(jtBusqueda);
        codigo.setMode(-1);
        codigo.setMode(0);
        accesoCodigo = 1;
        if (accesoDescripcion == 1) {
            descripcion.removeAllItems();
        }
        if (jrCodigo.isSelected() == true) {
            try {
                ResultSet rs_cod = conectar.listar("idproducto", "producto", "where estado = '1'");
                while (rs_cod.next() == true) {
                    codigo.addItem(rs_cod.getString(1));
                }
            } catch (Exception e) {
            }
        }
        conectar.Desconectado();
    }//GEN-LAST:event_jrCodigoActionPerformed

